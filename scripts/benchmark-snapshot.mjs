#!/usr/bin/env node
/**
 * Render a benchmark JSON report as markdown for the docs site.
 *
 * Usage:
 *   node scripts/benchmark-snapshot.mjs benchmark-report.json [output.md]
 */

import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname } from 'node:path';

const inputPath = process.argv[2] ?? 'benchmark-report.json';
const outputPath = process.argv[3] ?? 'docs/.vitepress/generated/benchmark-latest.md';

const report = JSON.parse(readFileSync(inputPath, 'utf8'));
const generatedAt = (report.timestamp ?? new Date().toISOString()).slice(0, 10);
const mode = report.quick ? 'quick' : 'full';

function formatRow(result) {
  return `| ${result.label} | \`${result.name}\` | ${result.value.toLocaleString()} | ${result.unit} |`;
}

const compareNames = new Set((report.competitive ?? []).map((entry) => entry.name));
const coreResults = report.results.filter((result) => !compareNames.has(result.name));
const compareResults = (report.competitive ?? []).filter(
  (result) => result && Number.isFinite(result.value),
);

const coreRows = coreResults.map(formatRow).join('\n');
const compareRows = compareResults.length > 0
  ? `\n\n### Competitive JSON (\`/bench\`)\n\n| Benchmark | Key | Value | Unit |\n|-----------|-----|------:|------|\n${compareResults
    .sort((left, right) => right.value - left.value)
    .map(formatRow)
    .join('\n')}`
  : '';

const markdown = `<!-- Generated by scripts/benchmark-snapshot.mjs — do not edit by hand. -->

## Latest CI snapshot (${mode} mode)

Node **${report.node}** on **${report.platform}/${report.arch}** — updated ${generatedAt}.

| Benchmark | Key | Value | Unit |
|-----------|-----|------:|------|
${coreRows}${compareRows}

[View workflow runs](https://github.com/pondoknusa/pondoknusa/actions/workflows/benchmarks.yml) for historical artifacts.
`;

mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(outputPath, markdown);
console.log(`Wrote ${outputPath}`);